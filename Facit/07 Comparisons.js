// 1. Choose the correct comparison operator to alert true, when x is greater than y.
let x = 10;
let y = 5;
alert(x > y);

// 2. Choose the correct comparison operator to alert true, when x2 is equal to y2.
const x2 = 10;
const y2 = 10;
alert(x2 == y2);

// 3. Choose the correct comparison operator to alert true, when x3 is NOT equal to y3.
x3 = 10;
y3 = 5;
alert(x3 != y3);

// 4. Choose the correct conditional (ternary) operator 
// ...to alert "Too young" if age is less than 18, 
// ...otherwise alert "Old enough".
let age = 19;
let voteable = (age < 18) ? "Too young" : "Old enough";
alert(voteable);